def random_chant():
    chance_of_chant = d4()
    if chance_of_chant == 4:
        return ""
    num_characters = max(0, 33 - d10() - d10() - d10())
    chant = ""
    for _ in range(num_characters):
        sound_roll = d100()
        chant += random_sound[sound_roll]
    return chant


def random_ingredients():
    first_roll = d100()
    second_roll = d100()
    if first_roll >= second_roll:
        return []
    number_of_ingredients = d4()
    ingredient_list = []
    for i in range(number_of_ingredients):
        ingredient_roll = d2000()
        ingredient_list.append(ingredient_table[ingredient_roll])
    return ingredient_list


def random_ritual(spell_level: int):
    chance = d100()
    if chance <= 10 * spell_level:
        return "Yes"
    return "No"


