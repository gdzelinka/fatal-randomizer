def lookup_race_social_class(race: str, roll: int) -> tuple[str, str, str]:
    race_table = race_social_class_table[race]
    for roll_key, values in race_table.items():
        if roll_key == "<11":
            if roll < 11:
                return values
        elif "-" in roll_key:
            low, high = (int(part) for part in roll_key.split("-", 1))
            if low <= roll <= high:
                return values
        elif roll == int(roll_key):
            return values
    raise KeyError(f"No social class entry for {race} roll {roll}")


